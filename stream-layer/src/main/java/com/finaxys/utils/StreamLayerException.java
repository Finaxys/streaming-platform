public class StreamLayerException extends RuntimeException {

    public StreamLayerException() {
        super();
    }

    public StreamLayerException(String message, Throwable cause) {
        super(message, cause);
    }

    public StreamLayerException(String message) {
        super(message);
    }

    public StreamLayerException(Throwable cause) {
        super(cause);
    }

}
